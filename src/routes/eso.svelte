  <div class="affBox-item">
    <h2>Collector Monitor Module</h2>
    <p class="affBox-text">
    The Collector Monitor module assists users in supervising collector productivity. The Collector Monitor module allows users to produce reports documenting collector performance statistics and view the current activities of designated collectors while they work in Debt$Net®. These features facilitate collector supervision and training and lets users analyze collector productivity and accountability.</p>
  </div>

  <div class="affBox-item">
    <h2>Document Imaging Module</h2>
    <p class="affBox-text">
    The Document Imaging Modulee lets users scan images (such as letterhead logos, NSF checks, etc.) directly into Debt$Net®. The scanned images are saved in the debtor, account, client, or contact manager files, where they may be accessed through Debt$Net® as needed.</p>
  </div>

  <div class="affBox-item">
    <h2>FoxFire! Report Writer Interface</h2>
    <p class="affBox-text">
    The FoxFire! Report Writer Interface module provides users with the tools to create and customize their own reports. Satisfy special client reporting requirements by using FoxFire! to filter out data items and to sort or group specified debtor, account, client and transaction information.</p>
  </div>

  <div class="affBox-item">
    <h2>UB04 Medical Form Interface</h2>
    <p class="affBox-text">
    The UB04 Medical Form Interface module allows you to enter debtor information on the UB04 health insurance claim form and to print the form directly from Debt$Net®. Once completed, the form may be submitted to the appropriate insurance company.</p>
