    <h1 id="top" >Extended Service Options</h1>

    <p>Debt$Net® collection software offers Extended Service Options, or ESOs. Extended Service Options are add-on modules that provide advanced functionality to collection agencies, including those with specific client-driven requirements. These add-on modules allow you to pay for only those features you require, letting Debt$Net® more fully automate your collection operations. Your agency may require none, or only a few of these Extended Service Options, however, for those who want power features, Extended Services options are available.</p>

    <h2 id="skip">Skip Tracing & Scoring</h2>

<div class="affBox">

  <div class="affBox-item">
    <h2>Accurint® Interface </h2>
    <img src="img/lexis.png" alt="lexis nexus logo">
    <p class="affBox-text">Accurint is a collection industry skip tracing service which maintains a comprehensive consumer database containing over 20 billion records compiled from more than 400 sources. At a minimum, these sources confirm existing records and offer multiple telephone, name and address records. <span> <br> Website: <br></span><a href="https://risk.lexisnexis.com/collections-and-recovery?utm_campaign=bsemgn18.em_vanity.2018vanity&utm_medium=vanityurl&utm_source=pdf&utm_content=collections&utm_term=collections/"> risk.lexisnexis.com</a></p>
  </div>


  <div class="affBox-item">
    <h2>BANKO® Interface Module </h2>
    <img src="img/lexis.png" alt="lexis nexus logo">
    <p class="affBox-text">The BANKO® Interface allows an agency access to bankruptcy cases and deceased debtor information.  This service consolidates and updates bankruptcy cases from all fifty states, the District of Columbia, and Puerto Rico daily, supplying timely, accurate, and complete bankruptcy information.</p>
  </div>

  <div class="affBox-item">
    <h2>Lexis Nexis Batch Interface </h2>
    <img src="img/lexis.png" alt="lexis nexus logo">
    <p class="affBox-text">The Lexis Nexis Batch Interface module receives daily updates from various sources such as telephone numbers, addresses, and emergence from bankruptcy directly from the source rather then from "self-importing" data. Lexis Nexis Batch is customized to work with the information and format you need.</p>
  </div>

  <div class="affBox-item">
    <h2>Credit Attributes Interface </h2>
    <img src="img/lexis.png" alt="lexis nexus logo">
    <p class="affBox-text">This interface provides a comprehensive view of your portfolio and provides flexible options that allow you to select the credit attributes most relevant to your agency's work flow needs. Credit Attributes for Collections can help your agency prioritize collection activities by identifying the customers most likely to pay through credit history, such as summaries of derogatory trades and other indicators of liquidity, including information on open credit and the presence of mortgage trades.</p>
  </div>

  <div class="affBox-item">
    <h2>Consumer Credit Reports</h2>
    <p class="affBox-text">The Consumer Credit Reports option allows your agency to request, pull and print credit reports on debtors as well as view them from the debtor screens in Debt$Net®. This is done on-line with a modem by calling into a credit bureau such as Equifax, Experian, or TransUnion®. The credit report can then be downloaded, saved, reviewed, printed, and/or transferred into the Debt$Net® debtor screens. This module also provides an activity log to track and monitor administrative costs.</p>
  </div>
  
  <div class="affBox-item">
    <h2>Experian® Collection Advantage</h2>
    <p class="affBox-text">The Experian Collection Advantage Extended Service Option (ESO) provides you access to an Experian service.  Data from collection activity files is forwarded to Experian, and they calculate a score to indicate how “collectable” the debtor’s account(s) are. In addition, Experian will provide service members with additional information (e.g., updated address) via a return file. <span> <br> Website: <br></span><a href="https://www.experian.com/access/index.html/"> www.experian.com</a></p>
  </div>

 <div class="affBox-item">
    <h2>Insight America Interface</h2>
    <p class="affBox-text">
    The Insight America Interface works with Acxiom Insight's Investigate product, a skip tracing tool that uses sophisticated data sets and customized analytics to find persons of interest. <span> <br> Website: <br></span><a href="https://www.acxiom.com/"> acxiom.com</a></p>
  </div>

  <div class="affBox-item">
    <h2>Sungard Predictive Metrics </h2>
    <img src="img/logo-sungard-availability-services.svg" alt="sungard logo">
    <p class="affBox-text">
    SunGard’s AvantGard Predictive Metrics can help increase profits by enhancing debt recovery success and strategically target accounts for legal. Debt collectors can use statistical scoring models to help prioritize collection efforts and improve collection strategies. <br> Website: <br><a href="https://www.sungardas.com/en-us/"> www.sungard.com</a></p>
  </div>

  <div class="affBox-item">
    <h2>TransUnion® CPE Module</h2>
    <p class="affBox-text">
    The TransUnion® Collector’s Prioritization Engine (CPE) is a service offered by TransUnion® in which data from collection activity files is analyzed.  TransUnion® uses the data to calculate a score to indicate how “collectable” the debtor’s account(s) are.  In addition, TransUnion® will provide service members with additional information (updated address, tradeline data, and feedback from the debtor) from their data files. Debt$Net® can capture this data from TransUnion® and attach it to the appropriate debtor record.  The debtor score can be used select accounts and reassign them, or used with Debt$Net®’s own work scoring process and used to prioritize collector’s work activities. Additional data is captured in extended references for the debtor. <br> Website: <br><a href="https://directcontent.transunion.com/servicesolutions/collectionsmgmt/segmentationPrioritization.page/"> www.tcn.com </a></p>
  </div>

  <div class="affBox-item">
    <h2>TransUnion® Collection Triggers</h2>
    <p class="affBox-text">
    The TransUnion®Collection Triggers Interface focuses on monitoring each consumer's credit file on a daily basis. TransUnion® will identify key changes to each consumer's credit file and contact information and compare those changes to a profile created for each customer. Your agency profile would consist of a selection of one or more trigger criteria, along with how often the triggered criteria should be returned to you.<br> Website: <br><a href="https://www.transunion.com/product/triggers/"> www.eftnetwork.com  </a></p>
  </div>

  <div class="affBox-item">
    <h2>NCOA Interface </h2>
    <img src="img/focus_logo.svg" alt="focus 1 logo">
    <img src="img/revSpring.svg" alt="rev spring logo">
    <p class="affBox-text">
    The NCOA module provides you the capability to process National Change of Address (NCOA) files. The files are created as part of the notice outsourcing process. Agencies which create notice files and forward them to outside vendors often receive back information on changed or incorrect addresses for debtors. The NCOA module will allow you to capture this information electronically.<br> Website: <br><a href="https://www.sungardas.com/en-us/"> www.sungard.com</a></p>
