    <h2>Client Upload/Client Input</h2>
    <p class="affBox-text">
    The Client Upload/Input Module allows for electronic loading of debtors and accounts into Debt$Net® with various file formats. The Client Input portion of the module gives your clients an interface which allows them to easily provide you with a file on diskette or via modem which contains their debtor/account information. Client Upload is then used to read the files and create debtor and account records in Debt$Net®.</p>
