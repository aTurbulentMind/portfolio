<h2 id="eva" >Software Evaluation Support</h2>

<p>The evaluation of collection agency software breaks down into these basic steps:</p>
<br>
<ul class="list">
    <li>Agency Requirements Analysis</li><br>
    <li>Preliminary Market Canvas</li><br>
    <li>Comprehensive Software Feature Analysis</li><br>
    <li>Company Investigation of Software Vendors</li>
</ul>
<br><br>
<p>Determine agency requirements by examining agency operations. Since most software will cover "normal" business practices, you will want to pay close attention to specialized functions of your agency. The evaluation should review the categories of collections your agency pursues (retail, medical, checks, commercial) including specialized markets such as debt buying and student loans. 
    <br><br><br><br>
    
Closely examine special and priority client requirements, agency financial deals, reporting and automated processes. Daily operations, such as data entry, payment processing, skip tracing, legal processing, notice generation and credit reports should be reviewed. 
<br><br><br><br>
Review accounting practices such as statements, trust accounting, month end processes, activity tracking, collector compensation and auditing. High-profile problems the agency is currently trying to resolve should be considered at this time as well. You will want to think about the growth plan of the agency and evaluate the adequacy of your existing equipment.
<br><br><br><br>
The preliminary market canvas boils down the long list of software vendors to just the top candidates which may suit agency operations. Analysis of software features can be time consuming, you have the ability to choose based on "best fit", look and feel. Check out product literature, websites, industry sources (trade magazines, trade conferences) and the recommendations from industry associates to narrow the list to a workable size.
<br><br><br><br>
Examine in depth the software features of the top candidates to ensure that agency requirements are met. Make a list of required features, or use our Debt Collection Software Evaluation Checklist to help you track the capabilities of each collection system. Acquire demonstration software and do evaluation walk-thrus to determine if the software works the way the agency requires; and that there are competent staff members of the software company to support the agency.
<br><br><br><br>
Having collection software with all the features the agency needs is not enough. It is important to research the competency, industry experience and business practices of the software vendor. Ascertain the company size, length of time in business, market share and industry presence. Inquire into their business practices for bug fixes, problem resolution and product advancement. Training, custom programming, technical support and emergency services are vital factors to maintaining the health of a collection agency.
