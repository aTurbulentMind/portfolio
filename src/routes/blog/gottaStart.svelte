<p> If you have to start somewhere, it might as well be from the bottom. Looking through a deployed airbag I could feel the solid floor. This bewildering point of beginning is everyone's favorite place to start. Or at least our most prevalent position for a life change. Even if we start facing traffic, it's still a place to start. Staring at the walls of problems we've found to make this crater.
</p>
<p>
 Pointing out one then another as the tiny things they are. If only those little bits became a check-off on the daily to-do list. If only I did not wait until moments before the due date to deal with each dilemma.  "If only's" get us no place fast. It lays out a godlike bounty of gained annoyance at what could have been. That's not fair to the human that you are right meow. 
</p>
<p><span>"my thought was not "oh fuck I am about to die" it was "FUCK, I don't have money to fix this"."</span></p>
<p>
I have found my face planted into my close compatriot the good 'old ground. The second my car started to hydroplane, the dust from the bedrock invaded my nostrils. After I was grateful for neither of my kids in the back seat my thought was not "oh fuck I am about to die" it was "FUCK, I don't have money to fix this". The sky collapsed with rain pressing a near disaster occurrence into my life and my mind spun with the cost. The cost. Not touching a car with my car in an unfriendly manner. Not those I say I love being with the memory of who I was. Not all of the things I could have done. The sentences my mind formed, in the most corrosive countenance, yelled at me about how much this cost us.
</p> 
<img class="port" src="/img/cartwo.jpg" alt="">
<p>
 There are no other voices that are not mine. Nothing telling me to cover my car in ketchup to ward off the aliens from Alpha Centauri. I needed my journey of self-sufficiency to start last year. Not 2021. The imposed last year that in fact could have been anytime before today. Before each evening gave the internal craving to be more much less merit. I need to be able to bounce between highway barriers and worry about my life, not the health situation of my account's vitality.
</p>
<p>
I have started to create an agency for web development. What better time to put in some effort than after a disaster. I have had the pleasure of enough employers in enough places to know how to and not to be.  I expect to fail along the way. And I am ok with that. We literally can't win them all.  But we can invest in our small victories. Every individual success seeds a new plant of hope. So these letters will be me germinating tomorrow's garden.
</p>
<img class="port" src="/img/carthree.jpg" alt="">
<p>
A few years have counted each day I should have started freelancing. I see articles or post's on the platforms that pretend to be social crying out joy. Their face lights up behind the screen and not from a backlight. Their whole body language shows in every letter on display. They have just started a new job in some web firm for XXXXXXXXXX dollars after the sixth month of coding. I should be excited to see somebodies life-changing for the better. And while I am for them, it only incites imposter syndrome confined in that coliseum built by collagen and calcium surrounding my precious grey matter.
</p> 
<p><span>"I want to believe that I want the best for everyone"</span></p>
<p>
I used to wonder why it upset me to see someone succeed. It made me question myself. I want to believe that I want the best for everyone. And to feel anger over somebody achieving in their life is the opposite of who I want to be. So I had to do some real self-searching about why that bothered me.  
</p>
<p>
The evidence of my annoyance seemed to start at the place I preverbally punch the ticket. From the person I deal with before my eyes open. And to that same being that is my thoughts until unconsciousness. Not that innocent to me random creature with a name and its own difficulties and hardships. They did not make my decisions. I did. And I want more than this microscopic amount of energy out of me to be more than just. I expected myself to have a bit figured out by now. How many videos have said, "you should be able to get a tech job within a year of learning".
</p> 
<img class="port" src="/img/car.jpg" alt="">
<p><span>"I have to make use of what I spent so much time learning"</span></p>
<p>
 Four years later... I am what has held me back. It does not matter how many times I'm denied in an interview. If I did not want this, I would have stopped long ago. I would not have invested anything if I did not believe in myself. So I have to make use of what I spent so much time learning. The lack of product sold is what granted my frustration so much force. Having to rely on the whim of whatever gold star name tagged willingly to throw their scraps into my famished account.
</p>
<p>
So, how do you change that? Make interactions with those around you. That's at least the first plan. Doing some pro-bono production to promote what you can do. On it. Now is that part that has grown strikingly uncomfortable. The waiting part. The time it takes to try to contact a thousand people is worth every painstaking moment. It won't be great to begin, but we all gotta start somewhere.
</p>

