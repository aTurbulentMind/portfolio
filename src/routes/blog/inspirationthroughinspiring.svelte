<p>Apologies. I did not set up internet at my house yet and my work wifi hates my laptop.</p>

<p>
    	There are a lot of languages to write for so many different reasons. The houses essentially are broken into three categories: frontend who make things look the way they do, programmers who make the front talk to the back, and backend dev who handle the databases and admin or structure of the program.  In the first few months, I thought I would be one of the magical full-stack devs who can do everything themselves. It took more time than I like to admit to come to the understanding that I don’t need to do everything. It is ok to have people for a task that I don’t like. I tried to learn some backend dev work and it just made me sleepy. Like, fall asleep behind the keyboard. So I don’t try. If there is something I have to be able to do, I try to learn it, but I don’t want to. Some people get as excited about the backend as I do about how the face of things looks. I can leave that to them.
	</p>
    <p>
	I chose early on not to go to school. I felt I could work on learning on my own at my pace. It takes a lot for me to accept that I don't only need myself. I grew up as an only child lacking "parental" figures so I had to be self-sufficient early. There are a few things that I missed the mark on. I don't have to be the center or focus of the room. The team is built out of the whole group. And those group connectivity moments are one of those fluid points of the future that become fixed points in the past. I also am not too huge a fan of doing things by myself. Not because I don’t think I can do it, but more of wanting to help people around me out of poverty also. Even if it is not to directly work with me and it's just a place to start. To add a bit of honesty, I am afraid of solo success. I need people near me. It's so inspiring. Even if near me is only through a screen.
	</p>

    <p><span>"I couldn't shake the gaze of the beast."</span></p>
    <p>
	I think most people like myself start with the basic HTML and CSS as their first place. Then you move up to JavaScript When I tried python. "Ooh, I like this a lot" was my reactionary response. But it never actually sunk its teeth into my flesh. Then like some strange creature that leaped brilliantly into view. I couldn't shake the gaze of the beast. It's so detailed and thought out. Its scope was intimidating. The process allowed 3d objects in the fucking web browser. 
	</p>
    <p>
	I tried to grasp at tutorials. Re-watching and repeating content years old trying to make sense. There is huge documentation available. I had not done any learning by documentation. It was tutorials at first. Then it became making things on my own and just looking for the answer to something I am stuck on. I think that part though is just part of being a dev though. People don’t remember everything. Some humans have great memory recall. That is not all of us.
	</p>
    <img src="/blogPic/inspirationThroughInspiring/insptwo.jpg" alt="the meows and i">
    <p>
	 Too many days backed up against each other and I have to break away from a project just to work on anything else for a few minutes. I made a mistake. I tried svelte. Well, sveltekit to be literal. No going back. No way I can look at anything else. It felt like something built for the future instead of feeling like learning through a dinosaur. Yes those old bones have their use. They have the backing and community and information to be able to do almost anything. But svelte had just enough to bring me into the fold. Like some cult leader had me in a trance.
	</p>
    <p>
	 It was probably three months into only svelte in my vision when I wanted to get back to three.js. It felt like going back to a day before cell phones. Archaic. The problem is that it's not. It's modern and has all the amenities. But all of sveltes magic made it hard to go to anything else. I tried for a few days to get things to work, but I eventually gave in the fight and set it aside on my desk to get back to. The love is there, but I wanted to focus on how to profit then do things for fun.
	</p>
    <p><span>"*choir music sounds light beaming out of every edge of the screen. It was like he was directly handing me a present."</span></p>
    <p>
	A few weeks pass and the main man Rich Harris during a presentation presents… dramatic pause. SvelteCubed *choir music sounds light beaming out of every edge of the screen. It was like he was directly handing me a present. I was so excited. I sprinted for the documentation  and found it bare bones. Sad face emoji. That’s lame. So I set some goals to get good with  svelte to be able to work into SvelteCubed once it was fledged out. Sike. One of the podcasts I listen to is called <a href="https://syntax.fm/"> Syntax</a>. A member of that show is named Scott Tolinski. Not only is he super nice, but he made a SvelteCubed course. First course I have ever purchased. Worth every dollar.
	</p>
    <img class="port" src="/blogPic/inspirationThroughInspiring/inspthree.jpg" alt="new orange outdoor cat">
    <p>
	He taught mostly about how to convert three.js into SvelteCubed. Yes it is based on JavaScript. No it is not 1:1. It has little things here and there that are different. So I opened and read through my first set of docs. I learned a bunch. It multiplied my love for Svelte. And now I feel so excited to build stuff.
	</p>
    <p>
	 I have a project for the company I work for that I need to finish. <a href="https://debtnet5build.netlify.app/">This is the project in process</a> . But it's been hard to work on something that is only one issue when I want to build new things. Why does Firefox have to break grid and svg elements so much. I have probably spent way to many hours trying to get an svg to be properly sized from menu bar to being enlarged. And the issue only lives in Firefox from what I've seen. I want to move on, but I need to figure out this issue to feel complete. I can't count how many projects I've started. I have only 100%ed a few of that pile. And there is a lot to learn in 100% completion. Little things that can break without looking back. 
	</p>

    <p>
	Not in work this week was all moving. We are transitioning from a duplex from the 30's to a house built in the 70's. It needs a bunch of work. It has not had the best maintenance, but that just makes for a blank slate. That’s pretty cool to be able to do whatever we pleaseish. I am gonna make a pen to have some chickens. I would have a cow if we had enough space. I love cows so much. But for now we just have a front and back yard to start. And I am thankful for it. Such an upgrade. 
	</p>
    <p>
    And I have been trying to slowly convert a few friends to code. One has taken the first step. And it really inspires me. It lights a fire under me. I want to be able to have enough work for people around me to profit. That makes us all better.  
    </p>

